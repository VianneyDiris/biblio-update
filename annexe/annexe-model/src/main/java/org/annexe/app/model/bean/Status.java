package org.annexe.app.model.bean;

public class Status {

	// ==================== Attributs ====================
	private Integer id;
	private String status;
		
	// ==================== Getters/Setters ====================
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
}
